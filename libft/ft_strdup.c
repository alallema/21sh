#include <stdlib.h>
#include <string.h>
#include "libft.h"

char		*ft_strdup(const char *src)
{
	char	*dest;
	int		i;

	i = 0;
	dest = ft_memalloc(sizeof(char) * (ft_strlen(src) + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
