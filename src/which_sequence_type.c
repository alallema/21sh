#include "ft_21sh.h"

int			is_number(char *s, int len)
{
	int		i;

	i = 0;
	if (s[0] == '+' || s[0] == '-')
		i += 1;
	while (i < len)
	{
		if (s[i] > '9' || s[i] < '0')
			return (FALSE);
		i++;
	}
	return (TRUE);
}
